package com.syntax.class05;

public class Recap {

	public static void main(String[] args) {
		// shortcut to autocomplete, main + ctrl+space
		
		/* We use .equals for Strings
		 * We use 	=	  for other variables
		*/
		
		String phone = "Samsung";

		if (phone.equals("Iphone")) {
			System.out.println("I want to buy it for me");
		} else if (phone.equals("Samsung")) {
			System.out.println("I want to buy it for my wife");
		} else if (phone.equals("Nokia")) {
			System.out.println("I want to buy it for my grandma");
		} else {
			System.out.println("I'm not buying anything");
		}

		
		
		
		
		
		
		
		
		
		
		
		
	}
}
