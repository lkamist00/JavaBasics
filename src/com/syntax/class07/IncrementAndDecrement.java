package com.syntax.class07;

public class IncrementAndDecrement {
	
	public static void main(String[] args) {
		
		int num=90;	
		num=num+1;
		System.out.println(num); //90+1
		
		//another way to increase/increment variable value by 1 -> ++
		
		num++; //num=num+1; 91+1
		System.out.println(num);
		
		//to decrease/decrement variable value by 1 -> --
		
		num--; //num=num-1; 91-1
		System.out.println(num);
		
		int i=10;
		i--;
		i--;
		//10-1-1
		System.out.println(i);
		
		
	}

}
